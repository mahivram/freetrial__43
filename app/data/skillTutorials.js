export const getYouTubeSearchQuery = (skill, level = 'beginner', language = 'hindi') => {
  const skillData = skillTutorialsData[skill];
  if (!skillData) {
    console.warn(`No data found for skill: ${skill}. Using generic search query.`);
    // Return a generic search query for unknown skills
    return `${skill} tutorial in hindi | ${skill} सीखने का तरीका`;
  }

  const languageQueries = skillData.searchQueries[language] || 
                         skillData.searchQueries[skillData.searchQueries.defaultLanguage];
  
  if (!languageQueries) {
    console.warn(`No queries found for language: ${language} in skill: ${skill}`);
    return `${skill} tutorial in hindi | ${skill} सीखने का तरीका`;
  }

  const query = languageQueries[level] || languageQueries.beginner;
  console.log('Generated query for', skill, level, ':', query);
  return query;
};

