// Helper function to construct YouTube search URL
export const constructYouTubeSearchURL = (query) => {
  const baseUrl = 'https://www.googleapis.com/youtube/v3/search';
  const params = new URLSearchParams({
    part: 'snippet',
    q: query,
    type: 'video',
    relevanceLanguage: 'hi',
    maxResults: '20',
    videoEmbeddable: 'true',
    videoDuration: 'long',
    order: 'relevance',
    regionCode: 'IN',
    safeSearch: 'moderate',
    
    videoCategoryId: '27', // Education category
    fields: 'items(id/videoId,snippet(title,description,thumbnails/high,channelTitle,publishedAt))'
  });
  params.append('sp', 'EgIQAQ%3D%3D'); 
  return `${baseUrl}?${params.toString()}`;
};
// To get all videos 4+ minutes (combining medium & long duration)
export const getYouTubeVideosAbove4Min = async (query) => {
  const mediumURL = constructYouTubeSearchURL(query, 'medium'); // 4-20 min
  const longURL = constructYouTubeSearchURL(query, 'long'); // 20+ min

  try {
    const [mediumResponse, longResponse] = await Promise.all([
      fetch(mediumURL).then(res => res.json()),
      fetch(longURL).then(res => res.json())
    ]);

    // Merge results from both durations
    return [...mediumResponse.items, ...longResponse.items];
  } catch (error) {
    console.error('Error fetching YouTube videos:', error);
    return [];
  }
};

